"""
Function to Find the Largest Number in a List
@author: msrak
@date: January 1, 2025

Description:
This function identifies and returns the largest number from a given list of numbers.


Example:
Input: [1, 2, 3, 4, 21, 12, 8]
Output: 21
"""


def largest_number(numbers):
    """
    Identifies and returns the largest number in the input list.

    Parameters:
    numbers (list): A list of numeric values to evaluate.

    Returns:
    int/float: The largest number in the list.
    """
    return max(numbers)
